    public void resaltarStockBajo() {
    tblInventario.setDefaultRenderer(Object.class, new javax.swing.table.DefaultTableCellRenderer() {
        @Override
        public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value,
                boolean isSelected, boolean hasFocus, int row, int column) {
            
            java.awt.Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            
            // Obtenemos los valores de las columnas Stock (3) y Mínimo (4)
            int stock = Integer.parseInt(table.getValueAt(row, 3).toString());
            int minimo = Integer.parseInt(table.getValueAt(row, 4).toString());

            if (stock < minimo) {
                c.setBackground(java.awt.Color.RED);
                c.setForeground(java.awt.Color.WHITE); // Texto blanco para que se lea mejor
            } else {
                // Si el stock es normal, usamos colores por defecto
                c.setBackground(isSelected ? table.getSelectionBackground() : table.getBackground());
                c.setForeground(isSelected ? table.getSelectionForeground() : table.getForeground());
            }
            return c;
        }
    });
}
