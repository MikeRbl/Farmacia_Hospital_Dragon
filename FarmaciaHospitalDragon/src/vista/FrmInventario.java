    private void tblInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInventarioMouseClicked
        // TODO add your handling code here:
        int fila = tblInventario.getSelectedRow();

     if (fila >= 0) {
         // Guardamos el ID en la variable global
         idInventarioSeleccionado = Integer.parseInt(tblInventario.getValueAt(fila, 0).toString());

         // Subimos los datos a las cajas de texto y combos
         txtMedicamento.setText(tblInventario.getValueAt(fila, 1).toString());
         cbxPresentacion.setSelectedItem(tblInventario.getValueAt(fila, 2).toString());
         txtStock.setText(tblInventario.getValueAt(fila, 3).toString());
         txtMinimo.setText(tblInventario.getValueAt(fila, 4).toString());
         cbxDistribucion.setSelectedItem(tblInventario.getValueAt(fila, 5).toString());
         txtVencimiento.setText(tblInventario.getValueAt(fila, 6).toString());
     }
    }//GEN-LAST:event_tblInventarioMouseClicked

