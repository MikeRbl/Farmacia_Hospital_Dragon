    private void tblPresentacionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPresentacionesMouseClicked
        // TODO add your handling code here:
        // Obtenemos la fila a la que le dieron clic (empiezan en 0)
     int fila = tblPresentaciones.getSelectedRow();

     if (fila >= 0) {
         // Sacamos el ID (Columna 0) y el Nombre (Columna 1)
         idPresentacionSeleccionada = Integer.parseInt(tblPresentaciones.getValueAt(fila, 0).toString());
         String nombre = tblPresentaciones.getValueAt(fila, 1).toString();

         // Ponemos el nombre en la caja de texto para que lo puedan editar
         txtNombrePresentacion.setText(nombre);
     }
    }//GEN-LAST:event_tblPresentacionesMouseClicked

